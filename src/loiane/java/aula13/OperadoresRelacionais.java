package loiane.java.aula13;

public class OperadoresRelacionais {
    public static void main(String[] args) {


        int x = 1;
        int y = 2;

        System.out.println("x == y " + (x == y));
        System.out.println("x != y " + (x != y));
        System.out.println("x > y " + (x > y));
        System.out.println("x < y " + (x < y));
        System.out.println("x <= y " + (x <= y));
        System.out.println("x >= y " + (x >= y));

    }
}
